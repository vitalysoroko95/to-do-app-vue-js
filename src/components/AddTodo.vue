<template>
	<form @submit.prevent="onSubmit">
		<input type="text" v-model="title">
		<button type="submit">Add todo</button>
	</form>
</template>


<script>
export default {
	data() {
		return {
			title: ''
		}
	},
	methods: {
		onSubmit() {
			if (this.title.trim()) {
				const newTodo = {
					_id: Date.now(),
					title: this.title,
					isCompleted: false
				}
				this.$emit('add-todo', newTodo)
				this.title = ''
			}
		}
	}
}
</script>

<style scoped>
form {
	display: flex;
	justify-content: center;

}

input {
	width: 400px
}
</style>